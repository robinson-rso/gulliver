                <h1>Sobre a Empresa</h1>
                <p>Estabelecida em Santos desde 1990, a Gulliver é hoje uma das maiores agências de viagens e turismo do Brasil.

Além de exercer as atividades regulares de agências de viagens é também uma operadora turística.

Em 1992 a empresa iniciou a condução de grupos de jovens para Fernando de Noronha e Ilhas Virgens.

Sempre com uma orientação de respeito, cuidado e carinho com seus clientes, a Gulliver se tornou referência no destino e a empresa líder no segmento de Eco Turismo.
